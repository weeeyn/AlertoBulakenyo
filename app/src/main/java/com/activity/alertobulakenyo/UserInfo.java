package com.activity.alertobulakenyo;

public class UserInfo {

    String firstname, lastname, username, contact, house, city, brgy, province, email;

    public UserInfo() {
    }

    public UserInfo(String firstname, String lastname, String username, String contact, String house, String city, String brgy, String province, String email) {
        this.firstname = firstname;
        this.lastname = lastname;
        this.username = username;
        this.contact = contact;
        this.house = house;
        this.city = city;
        this.brgy = brgy;
        this.province = province;
        this.email = email;
    }

    public String getFirstname() {
        return firstname;
    }

    public void setFirstname(String firstname) {
        this.firstname = firstname;
    }

    public String getLastname() {
        return lastname;
    }

    public void setLastname(String lastname) {
        this.lastname = lastname;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getContact() {
        return contact;
    }

    public void setContact(String contact) {
        this.contact = contact;
    }

    public String getHouse() {
        return house;
    }

    public void setHouse(String house) {
        this.house = house;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getBrgy() {
        return brgy;
    }

    public void setBrgy(String brgy) {
        this.brgy = brgy;
    }

    public String getProvince() {
        return province;
    }

    public void setProvince(String province) {
        this.province = province;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
